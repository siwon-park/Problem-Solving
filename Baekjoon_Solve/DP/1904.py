    # 문제: https://www.acmicpc.net/problem/1904
    # DP(다이나믹 프로그래밍)
    # dp[i]=dp[i-1]에 1을 양 옆에 각 각 붙이는 케이스+ dp[i-2]에 00을 양 옆에 각 각 붙이는 케이스 - 중복인데
    # 점화식은 dp[i]=dp[i-1]+dp[i-2]로 피보나치 수열이다
####################################################
import sys
input=sys.stdin.readline
N=int(input())
dp=[0]*1000001
dp[1],dp[2]=1,2
for i in range(3,N+1):
    dp[i]=(dp[i-1]+dp[i-2])%15746
print(dp[N])    

# 플러그(2010번)
##################################################################################
    # 문제: https://www.acmicpc.net/problem/2010
    # 수학, 구현
    # 초기에 플러그가 1개이고, 멀티탭을 해당 플러그에 꽂으면 -= 1이 된다.
    # 마찬가지로, 멀티탭에 멀티탭을 꽂으면 -= 1씩 줄어든다.
##################################################################################
import sys
input = sys.stdin.readline

N = int(input().rstrip())
cnt = 1
for _ in range(N):
    num = int(input().rstrip())
    cnt -= 1
    cnt += num

print(cnt)
